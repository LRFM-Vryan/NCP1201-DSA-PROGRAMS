#include <iostream>
using namespace std;
int main() {
int i = 1;
while (i <= 3) {
cout << i << endl;
i++;
}
return 0;
}

// This program demonstrates the use of a while loop in C++
// It initializes a variable 'i' to 1 and prints its value
// while 'i' is less than or equal to 3, incrementing 'i'
// in each iteration. You can modify the loop condition and
// the increment step to see different behaviors.