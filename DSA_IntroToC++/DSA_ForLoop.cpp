#include <iostream>
using namespace std;
int main() {
for (int i = 1; i <= 5; i++) {
cout << "Count: " << i << endl;
}
return 0;
}

// This program demonstrates the use of a for loop in C++
// It iterates from 1 to 5, printing the current count in each iteration.
// You can modify the loop parameters to see different behaviors.
