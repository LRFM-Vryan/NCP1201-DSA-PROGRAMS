#include <iostream>
using namespace std;
int main() {
int arr[5] = {1, 2, 3, 4, 5};
for (int i = 0; i < 5; i++) {
cout << arr[i] << " ";
}
return 0;
}

// this program demonstrates how to loop through an array in C++ using a for loop.
// It initializes an array of five integers and prints each element to the console.
// every iteration of the loop accesses the array element at index i and outputs its value.